<h2>Evo luontopolku</h2>
<p>
	Evo luontopolku was a school project to make a map / GPS application. Application 
	shows user a map of the surrounding area of the nature trail and tracks user movement on the trail 
	with GPS. It also draws a line of the path travelled.
</p>
<p>
	When nearing some certain point on the path, application pops up a information window.
	From that window user can read some information about the current place.
</p>
<p>
	Development was made during school courses and the application it self was written with Java ME.
</p>

