<strong>Sky rules</strong>:
<ul>
<li>Sky events are on tuesdays at 6:00PM (18:00) and saturdays 3:00PM (15:00) GMT.</li>
<li>Everyone puts up comment what they want to lot, if they have enough points to lot.</li>
<li>You must be able to use that item.</li>
<li>If you are at sky 30 minutes before event start you get one extra point.</li>
<li>You can use all your points for one item, if you have most points of lotters who also want that item.</li>
<li>You lose points only if that item you want drops.</li>
</ul>
<p>
<b>Example:</b> MisterX has 10 points and LadyZ 11 points, 
they both want to lot X Abjuration. LadyZ really wants that 
item and she is ready to use all her points for it. So she loses 
all her 11 points and is only one lotting X Abjuration.<br />
<br />
If they both decide they don't want to use all of their points, 
then it's the same as before. Everyone who can use and has comment can 
lot and winner loses 3 points.
</p>
<ul>
<li>Being early: +1 point</li>
<li>Farming: +2 points</li>
<li>Defeating god: +1 point</li>
<li>Lotting: -5 points</li>
</ul>
<p>
